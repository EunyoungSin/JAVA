public class CharExam {
    public static void main(String[] args) {
        int sum=0; // '0'->48
        sum=sum+('1'-'0'); // '1'->49
        sum=sum+('2'-'0');
        sum=sum+('3'-'0');
        sum=sum+('4'-'0');
        sum=sum+('5'-'0');
        System.out.println("sum = " + sum);
        System.out.println(); // 개행, 줄바꿈
    }
}
