package fc.java.part2;

public class FindAPI {
    public static void main(String[] args) {
        int a;
        // AAA b;
        a=10;
        System.out.println("a = " + a);
        // java>lang 안의 클래스는 자주 사용하는 클래스라 위치 생략가능
        java.lang.String s;
        s="APPLE";
        System.out.println("s = "+ s);
        System.out.println(s.length());
    }
}
